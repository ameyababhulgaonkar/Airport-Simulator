package com.as.Aircraft;



public class AircraftType {
	public String nameOfAircraft;
	public int runwayTime;
	public int boardingTime;
	public String getNameOfAircraft() {
		return nameOfAircraft;
	}
	public void setNameOfAircraft(String nameOfAircraft) {
		this.nameOfAircraft = nameOfAircraft;
	}
	public int getRunwayTime() {
		return runwayTime;
	}
	public void setRunwayTime(int runwayTime) {
		this.runwayTime = runwayTime;
	}
	public int getBoardingTime() {
		return boardingTime;
	}
	public void setBoardingTime(int boardingTime) {
		this.boardingTime = boardingTime;
	}

}
